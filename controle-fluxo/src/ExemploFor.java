
public class ExemploFor {
	public static void main(String[] args) {
		// Estrutura do fluxo "for"
		// Bloco de inicialização (valor de carneirinhos começa igual a 1);
		// Bloco de expressão da condição a ser validada (até menor ou igual a 20)
		// Bloco de atualização
		for (int carneirinhos = 1; carneirinhos <= 20; carneirinhos++) {
			System.out.println(carneirinhos + " - Carneirinho(s)");
		}

		// Mensagem para confirmar se a condição foi encerrada
		System.out.println("Joãozinho dormiu");
	}
}